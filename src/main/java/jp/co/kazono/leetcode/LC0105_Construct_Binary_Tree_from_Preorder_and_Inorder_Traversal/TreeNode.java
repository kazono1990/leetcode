package jp.co.kazono.leetcode.LC0105_Construct_Binary_Tree_from_Preorder_and_Inorder_Traversal;

public class TreeNode {
    int val;
    TreeNode left;
    TreeNode right;
    TreeNode() {}
    TreeNode(int val) { this.val = val; }
    TreeNode(int val, TreeNode left, TreeNode right) {
        this.val = val;
        this.left = left;
        this.right = right;
    }
}
